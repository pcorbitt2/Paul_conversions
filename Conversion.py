# My conversion tools

#Converting Dollars to cents

def dollar2cents(dollars):
	cents = dollars * 100
	return(cents)
	
#Converting Gallong to Liters

def gallons2liters(gallons):
	liters = gallons / 3.78
	return(liters)

def feet2inches(feet):
	inches = 12 * feet
	return(inches)
	
#Converting Hours to Minutes
def hours2minutes(hours):
	minutes = hours * 60
	return minutes
